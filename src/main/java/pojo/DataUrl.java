package pojo;
//DataUrl contains getter() and setter() method name and and url foe web scraping
public class DataUrl {
	private String Name;
	private String url;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
